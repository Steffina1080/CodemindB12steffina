package javaPractices;

public class NestedStatement {

	public static void main(String[] args) {
		int chocolate=10;
		
		if (chocolate==1) {
			System.out.println("I will eat it");
		}
		else if (chocolate==2) {
			System.out.println("I will eat one and share one with my brother");
			
		}
		else if (chocolate==3) {
			System.out.println("I will eat one, share one with my brother and share another with my sister");
		}
		else {
			System.out.println("I will share all with my friends");
		}

	}

}
